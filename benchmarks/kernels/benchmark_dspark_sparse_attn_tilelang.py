"""Benchmark DSpark TileLang sparse attention candidate shapes.

This is a development-only harness for selecting the static TileLang shape used
by DeepSeek V4 DSpark draft attention. TileLang does not provide a Triton-style
runtime autotuner for this kernel, so the safe way to prove a config is optimal
is to compile candidate variants, validate them against a PyTorch reference, and
measure CUDA-event latency for the real runtime shapes.

Example:

    python benchmarks/kernels/benchmark_dspark_sparse_attn_tilelang.py \
        --real-heads 32 --kernel-heads 32 --blocks 16,24,32,48,64

    python benchmarks/kernels/benchmark_dspark_sparse_attn_tilelang.py \
        --real-heads 16 --kernel-heads 64 --blocks 16,32,48,64
"""

from __future__ import annotations

import argparse
import gc
import hashlib
import importlib.util
import json
import os
from pathlib import Path
import sys
import textwrap
import traceback
from dataclasses import asdict, dataclass
from functools import cache
from itertools import product
from typing import Any

import torch


def _parse_csv_ints(value: str) -> list[int]:
    return [int(x.strip()) for x in value.split(",") if x.strip()]


def _parse_csv_strings(value: str) -> list[str]:
    return [x.strip() for x in value.split(",") if x.strip()]


@dataclass(frozen=True)
class Candidate:
    kernel_heads: int
    head_dim: int
    block: int
    threads: int
    num_stages: int
    qk_policy: str
    pv_policy: str


@dataclass
class Result:
    candidate: dict[str, Any]
    ok: bool
    mean_ms: float | None = None
    p50_ms: float | None = None
    min_ms: float | None = None
    max_ms: float | None = None
    max_abs: float | None = None
    mean_abs: float | None = None
    error: str | None = None


def _reference(
    q: torch.Tensor,
    kv: torch.Tensor,
    attn_sink: torch.Tensor,
    topk_idxs: torch.Tensor,
    softmax_scale: float,
) -> torch.Tensor:
    valid = topk_idxs >= 0
    gather_idxs = topk_idxs.clamp_min(0).long()
    batch = torch.arange(kv.shape[0], device=kv.device).view(-1, 1, 1)
    gathered_kv = kv[batch, gather_idxs]

    scores = torch.einsum("bmhd,bmkd->bmhk", q.float(), gathered_kv.float())
    scores.mul_(softmax_scale)
    scores.masked_fill_(~valid.unsqueeze(2), -torch.inf)

    scores_max = scores.amax(dim=-1)
    weights = torch.exp(scores - scores_max.unsqueeze(-1))
    weights.masked_fill_(~valid.unsqueeze(2), 0.0)
    denom = weights.sum(dim=-1)
    denom.add_(torch.exp(attn_sink.float().view(1, 1, -1) - scores_max))

    out = torch.einsum("bmhk,bmkd->bmhd", weights, gathered_kv.float())
    out.div_(denom.unsqueeze(-1))
    return out.to(q.dtype)


@cache
def _build_kernel(candidate: Candidate, softmax_scale: float):
    valid_policies = {"FullRow", "FullCol", "Square"}
    if candidate.qk_policy not in valid_policies:
        raise ValueError(f"unknown qk policy: {candidate.qk_policy}")
    if candidate.pv_policy not in valid_policies:
        raise ValueError(f"unknown pv policy: {candidate.pv_policy}")

    payload = {"candidate": asdict(candidate), "softmax_scale": softmax_scale}
    digest = hashlib.sha256(
        json.dumps(payload, sort_keys=True).encode("utf-8")
    ).hexdigest()[:16]
    cache_root = Path(
        os.environ.get(
            "DSPARK_TILELANG_BENCH_CACHE",
            str(Path(os.environ.get("XDG_CACHE_HOME", "~/.cache")).expanduser()
                / "dspark_tilelang_bench"),
        )
    )
    cache_root.mkdir(parents=True, exist_ok=True)
    module_name = f"_dspark_tilelang_bench_{digest}"
    module_path = cache_root / f"{module_name}.py"
    source = _candidate_module_source(candidate, softmax_scale)
    if not module_path.exists() or module_path.read_text() != source:
        module_path.write_text(source)

    spec = importlib.util.spec_from_file_location(module_name, module_path)
    if spec is None or spec.loader is None:
        raise RuntimeError(f"could not load generated module {module_path}")
    module = importlib.util.module_from_spec(spec)
    sys.modules[module_name] = module
    spec.loader.exec_module(module)
    return module.build_kernel()


def _candidate_module_source(candidate: Candidate, softmax_scale: float) -> str:
    """Return a literal TileLang module for one candidate.

    TileLang's source parser is sensitive to Python closures for this kernel.
    Generating a tiny importable module with literal constants keeps the
    benchmark independent from that parser limitation and mirrors the runtime
    kernel structure.
    """
    return textwrap.dedent(
        f"""
        # Auto-generated by benchmark_dspark_sparse_attn_tilelang.py.
        from typing import Any

        import tilelang
        import tilelang.language as T


        def build_kernel():
            pass_configs: dict[tilelang.PassConfigKey, Any] = {{
                tilelang.PassConfigKey.TL_DISABLE_WARP_SPECIALIZED: True,
                tilelang.PassConfigKey.TL_DISABLE_TMA_LOWER: True,
            }}

            bf16 = "bfloat16"
            fp32 = "float32"
            int32 = "int32"

            @tilelang.jit(pass_configs=pass_configs)
            def kernel(h: int, d: int, softmax_scale: float):
                b = T.symbolic("b")
                m = T.symbolic("m")
                n = T.symbolic("n")
                topk = T.symbolic("topk")
                block = {candidate.block}
                num_stages = {candidate.num_stages}
                threads = {candidate.threads}
                num_blocks = tilelang.cdiv(topk, block)

                @T.prim_func
                def sparse_attn_kernel_(
                    q: T.Tensor[(b, m, h, d), bf16],
                    kv: T.Tensor[(b, n, d), bf16],
                    out: T.Tensor[(b, m, h, d), bf16],
                    attn_sink: T.Tensor[(h,), fp32],
                    topk_idxs: T.Tensor[(b, m, topk), int32],
                ):
                    with T.Kernel(m, b, threads=threads) as (bx, by):
                        q_shared = T.alloc_shared((h, d), bf16)
                        kv_shared = T.alloc_shared((block, d), bf16)
                        out_shared = T.alloc_shared((h, d), bf16)
                        acc_s_cast = T.alloc_shared((h, block), bf16)

                        idxs = T.alloc_fragment(block, int32)
                        acc_s = T.alloc_fragment((h, block), fp32)
                        acc_o = T.alloc_fragment((h, d), fp32)
                        scores_max = T.alloc_fragment(h, fp32)
                        scores_max_prev = T.alloc_fragment(h, fp32)
                        scores_scale = T.alloc_fragment(h, fp32)
                        scores_sum = T.alloc_fragment(h, fp32)
                        sum_exp = T.alloc_fragment(h, fp32)

                        T.clear(acc_o)
                        T.clear(sum_exp)
                        T.fill(scores_max, -T.infinity(fp32))
                        T.copy(q[by, bx, :, :], q_shared)

                        for t in T.Pipelined(num_blocks, num_stages=num_stages):
                            for i in T.Parallel(block):
                                idxs[i] = T.if_then_else(
                                    t * block + i < topk,
                                    topk_idxs[by, bx, t * block + i],
                                    -1,
                                )
                            for i, j in T.Parallel(block, d):
                                kv_shared[i, j] = T.if_then_else(
                                    idxs[i] != -1,
                                    kv[by, idxs[i], j],
                                    0,
                                )
                            for i, j in T.Parallel(h, block):
                                acc_s[i, j] = T.if_then_else(
                                    idxs[j] != -1,
                                    0,
                                    -T.infinity(fp32),
                                )
                            T.gemm(
                                q_shared,
                                kv_shared,
                                acc_s,
                                transpose_B=True,
                                policy=T.GemmWarpPolicy.{candidate.qk_policy},
                            )
                            for i, j in T.Parallel(h, block):
                                acc_s[i, j] *= softmax_scale
                            T.copy(scores_max, scores_max_prev)
                            T.reduce_max(acc_s, scores_max, dim=1, clear=False)
                            for i in T.Parallel(h):
                                scores_scale[i] = T.exp(scores_max_prev[i]
                                                        - scores_max[i])
                            for i, j in T.Parallel(h, block):
                                acc_s[i, j] = T.exp(acc_s[i, j] - scores_max[i])
                            T.reduce_sum(acc_s, scores_sum, dim=1)
                            for i in T.Parallel(h):
                                sum_exp[i] = (sum_exp[i] * scores_scale[i]
                                              + scores_sum[i])
                            T.copy(acc_s, acc_s_cast)
                            for i, j in T.Parallel(h, d):
                                acc_o[i, j] *= scores_scale[i]
                            T.gemm(
                                acc_s_cast,
                                kv_shared,
                                acc_o,
                                policy=T.GemmWarpPolicy.{candidate.pv_policy},
                            )

                        for i in T.Parallel(h):
                            sum_exp[i] += T.exp(attn_sink[i] - scores_max[i])
                        for i, j in T.Parallel(h, d):
                            acc_o[i, j] /= sum_exp[i]
                        T.copy(acc_o, out_shared)
                        T.copy(out_shared, out[by, bx, :, :])

                return sparse_attn_kernel_

            return kernel({candidate.kernel_heads}, {candidate.head_dim},
                          {softmax_scale!r})
        """
    )


def _make_inputs(args: argparse.Namespace) -> tuple[torch.Tensor, ...]:
    torch.manual_seed(args.seed)
    device = torch.device("cuda")
    q = torch.randn(
        args.batch,
        args.draft_tokens,
        args.real_heads,
        args.head_dim,
        device=device,
        dtype=torch.bfloat16,
    )
    kv = torch.randn(
        args.batch,
        args.kv_len,
        args.head_dim,
        device=device,
        dtype=torch.bfloat16,
    )
    attn_sink = torch.randn(
        args.real_heads,
        device=device,
        dtype=torch.float32,
    )
    topk_idxs = torch.randint(
        0,
        args.kv_len,
        (args.batch, args.draft_tokens, args.topk),
        device=device,
        dtype=torch.int32,
    )
    if args.invalid_tail:
        topk_idxs[..., -args.invalid_tail:] = -1
    return q, kv, attn_sink, topk_idxs


def _pad_heads(
    q: torch.Tensor,
    attn_sink: torch.Tensor,
    kernel_heads: int,
) -> tuple[torch.Tensor, torch.Tensor]:
    real_heads = q.shape[2]
    if kernel_heads == real_heads:
        return q.contiguous(), attn_sink.contiguous()
    if kernel_heads < real_heads:
        raise ValueError(
            f"kernel_heads={kernel_heads} is smaller than real_heads={real_heads}"
        )
    q_pad = torch.cat(
        [q, q.new_zeros(q.shape[0], q.shape[1], kernel_heads - real_heads, q.shape[3])],
        dim=2,
    )
    sink_pad = torch.cat([attn_sink, attn_sink.new_zeros(kernel_heads - real_heads)])
    return q_pad.contiguous(), sink_pad.contiguous()


def _benchmark_candidate(
    candidate: Candidate,
    q: torch.Tensor,
    kv: torch.Tensor,
    attn_sink: torch.Tensor,
    topk_idxs: torch.Tensor,
    ref: torch.Tensor,
    softmax_scale: float,
    warmup: int,
    iters: int,
    max_abs_tol: float,
    mean_abs_tol: float,
) -> Result:
    try:
        q_pad, sink_pad = _pad_heads(q, attn_sink, candidate.kernel_heads)
        kv = kv.contiguous()
        topk_idxs = topk_idxs.contiguous()
        out = torch.empty_like(q_pad)

        kernel = _build_kernel(candidate, softmax_scale)
        kernel(q_pad, kv, out, sink_pad, topk_idxs)
        torch.cuda.synchronize()

        actual = out.narrow(2, 0, q.shape[2])
        diff = (actual.float() - ref.float()).abs()
        max_abs = float(diff.max().item())
        mean_abs = float(diff.mean().item())
        if max_abs > max_abs_tol or mean_abs > mean_abs_tol:
            return Result(
                candidate=asdict(candidate),
                ok=False,
                max_abs=max_abs,
                mean_abs=mean_abs,
                error=(
                    "failed correctness tolerance: "
                    f"max_abs={max_abs:.6g} > {max_abs_tol:.6g} or "
                    f"mean_abs={mean_abs:.6g} > {mean_abs_tol:.6g}"
                ),
            )

        for _ in range(warmup):
            kernel(q_pad, kv, out, sink_pad, topk_idxs)
        torch.cuda.synchronize()

        samples = []
        for _ in range(iters):
            start = torch.cuda.Event(enable_timing=True)
            end = torch.cuda.Event(enable_timing=True)
            start.record()
            kernel(q_pad, kv, out, sink_pad, topk_idxs)
            end.record()
            end.synchronize()
            samples.append(float(start.elapsed_time(end)))

        samples_sorted = sorted(samples)
        return Result(
            candidate=asdict(candidate),
            ok=True,
            mean_ms=sum(samples) / len(samples),
            p50_ms=samples_sorted[len(samples_sorted) // 2],
            min_ms=samples_sorted[0],
            max_ms=samples_sorted[-1],
            max_abs=max_abs,
            mean_abs=mean_abs,
        )
    except Exception as exc:
        return Result(
            candidate=asdict(candidate),
            ok=False,
            error=f"{type(exc).__name__}: {exc}\n{traceback.format_exc(limit=3)}",
        )
    finally:
        gc.collect()
        torch.cuda.empty_cache()


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--real-heads", type=int, required=True)
    parser.add_argument("--kernel-heads", type=int, default=None)
    parser.add_argument("--head-dim", type=int, default=512)
    parser.add_argument("--batch", type=int, default=1)
    parser.add_argument("--draft-tokens", type=int, default=5)
    parser.add_argument("--kv-len", type=int, default=4096)
    parser.add_argument("--topk", type=int, default=128)
    parser.add_argument("--blocks", default="16,24,32,48,64")
    parser.add_argument("--threads", default="128,256,512")
    parser.add_argument("--num-stages", default="1,2,3")
    parser.add_argument("--qk-policies", default="FullRow,FullCol,Square")
    parser.add_argument("--pv-policies", default="FullRow,FullCol,Square")
    parser.add_argument("--warmup", type=int, default=10)
    parser.add_argument("--iters", type=int, default=50)
    parser.add_argument("--max-abs-tol", type=float, default=0.01)
    parser.add_argument("--mean-abs-tol", type=float, default=0.001)
    parser.add_argument("--seed", type=int, default=0)
    parser.add_argument("--invalid-tail", type=int, default=0)
    parser.add_argument("--jsonl", default="")
    args = parser.parse_args()

    torch.cuda.set_device(0)
    softmax_scale = args.head_dim ** -0.5
    kernel_heads = args.kernel_heads or args.real_heads

    q, kv, attn_sink, topk_idxs = _make_inputs(args)
    ref = _reference(q, kv, attn_sink, topk_idxs, softmax_scale)
    torch.cuda.synchronize()

    candidates = [
        Candidate(
            kernel_heads=kernel_heads,
            head_dim=args.head_dim,
            block=block,
            threads=threads,
            num_stages=num_stages,
            qk_policy=qk_policy,
            pv_policy=pv_policy,
        )
        for block, threads, num_stages, qk_policy, pv_policy in product(
            _parse_csv_ints(args.blocks),
            _parse_csv_ints(args.threads),
            _parse_csv_ints(args.num_stages),
            _parse_csv_strings(args.qk_policies),
            _parse_csv_strings(args.pv_policies),
        )
    ]

    results: list[Result] = []
    out_file = open(args.jsonl, "w", encoding="utf-8") if args.jsonl else None
    try:
        for candidate in candidates:
            result = _benchmark_candidate(
                candidate,
                q,
                kv,
                attn_sink,
                topk_idxs,
                ref,
                softmax_scale,
                args.warmup,
                args.iters,
                args.max_abs_tol,
                args.mean_abs_tol,
            )
            results.append(result)
            payload = asdict(result)
            print(json.dumps(payload, sort_keys=True), flush=True)
            if out_file is not None:
                out_file.write(json.dumps(payload, sort_keys=True) + "\n")
                out_file.flush()
    finally:
        if out_file is not None:
            out_file.close()

    valid = [r for r in results if r.ok and r.mean_ms is not None]
    if valid:
        best = min(valid, key=lambda r: r.mean_ms or float("inf"))
        print("\nBest candidate:")
        print(json.dumps(asdict(best), indent=2, sort_keys=True))
    else:
        print("\nNo valid candidate compiled and passed correctness.")


if __name__ == "__main__":
    main()
